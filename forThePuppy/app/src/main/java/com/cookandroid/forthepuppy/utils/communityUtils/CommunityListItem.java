package com.cookandroid.forthepuppy.utils.communityUtils;

public class CommunityListItem {
    private String title, body;

    public CommunityListItem(String title, String body){
        this.title = title;
        this.body = body;

    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
